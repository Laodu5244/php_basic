<?php

// 1、布尔型
$bool = true;
$boolean = false;

// var_dump 可以打印数据的类型和值
var_dump($bool);
echo '<br>';
var_dump($boolean);
echo '<br>';

// 2、整型
$num1 = 0;
var_dump($num1);
echo '<br>';

$num2 = 53;
var_dump($num2);
echo '<br>';

$num3 = -99;
var_dump($num3);
echo '<br>';

// 3、浮点型
$num4 = 10.23;
var_dump($num4);
echo '<br>';

$num5 = -123.444;
var_dump($num5);
echo '<br>';

# 字符串
$str1 = '我是欧阳峰';
var_dump($str1);
echo '<br>';

$str2 = '13423';
var_dump($str2);
echo '<br>';

$str3 = "false";
var_dump($str3);
echo '<br>';

// 特殊类型
$null = null;
var_dump($null);
?>